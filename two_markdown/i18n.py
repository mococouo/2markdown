RTL_LANGUAGES = {"ar"}

_current = "en"


def available_languages() -> list[tuple[str, str]]:
    return [(code, DISPLAY_NAMES.get(code, code)) for code in LANGUAGES]


def set_language(code: str) -> None:
    global _current
    if code in LANGUAGES:
        _current = code
    else:
        base = code.split("-")[0]
        _current = base if base in LANGUAGES else "en"


def current_language() -> str:
    return _current


def t(key: str, **kwargs: Any) -> str:
    table = LANGUAGES.get(_current) or LANGUAGES["en"]
    text = table.get(key)
    if text is None:
        text = LANGUAGES["en"].get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError):
            return text
    return text


def is_rtl(code: str | None = None) -> bool:
    return (code or _current) in RTL_LANGUAGES
